# Find for tecmint.txt in the /home directory
find /home -iname tecmint.txt

# Find repo named diretories
find . -type d -name "*_repo"

# Find all PHP Files in Directory
find . -type f -name "*.php"

# Lists all diretories
find . -type d

# Lists all files
find . -type f